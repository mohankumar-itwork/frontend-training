// arthmetic operators
var x = 5;
var y = 3;
console.log("x + y = " + (x + y)); // addition
console.log("x - y = " + (x - y)); // subtraction
console.log("x * y = " + (x * y)); // multiplication
console.log("x / y = " + (x / y)); // division
console.log("x % y = " + (x % y)); // modulus 
console.log("x ** y = " + (x ** y)); // exponentiation

// assignment operators    
var z = 10;
console.log("z = " + z);
z += 5; // equivalent to z = z + 5
console.log("z += 5 gives: " + z);
z -= 3; // equivalent to z = z - 3
console.log("z -= 3 gives: " + z);
z *= 2; // equivalent to z = z * 2
console.log("z *= 2 gives: " + z);
z /= 4; // equivalent to z = z / 4
console.log("z /= 4 gives: " + z);

// comparison operators
var a = 10;
var b = 20;
console.log("a == b: " + (a == b)); // equality
console.log("a != b: " + (a != b)); // inequality
console.log("a > b: " + (a > b)); // greater than
console.log("a < b: " + (a < b)); // less than
console.log("a >= b: " + (a >= b)); // greater than or equal to
console.log("a <= b: " + (a <= b)); // less than or equal to

// logical operators
var p = true;
var q = false;
console.log("p && q: " + (p && q)); // logical AND
console.log("p || q: " + (p || q)); // logical OR
console.log("!p: " + (!p)); // logical NOT  

